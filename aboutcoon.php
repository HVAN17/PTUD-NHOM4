    <h3> Cam kết</h3>
    <p>100% nguyên liệu có nguồn gốc rõ ràng và an toàn cho làn da: đây là lời hứa và cam kết tuyệt đối của chúng tôi. Tất cả thành phần nguyên liệu trong các sản phẩm của chúng tôi đều có chứng từ chứng minh nguồn gốc xuất xứ từ các nhà cung cấp nguyên liệu trong và ngoài nước. Tất cả các sản phẩm mỹ phẩm trước khi được đưa ra thị trường đều được nghiên cứu từ 12 đến 24 tháng, được thử nghiệm để vượt qua các bài kiểm tra về vi sinh, pH, độ ổn định theo thời gian, theo nhiệt độ, độ kích ứng (theo tiêu chuẩn của trung tâm DRC Nhật Bản có chi nhánh tại Thái Lan) và phải đáp ứng đầy đủ các quy định và việc lưu thông trên thị trường theo quy định của Bộ Y tế Việt Nam. Tất nhiên, các sản phẩm của chúng tôi được điều chế sẽ không có các thành phần như paraben, formaldehyde, phthalates, hydroquinone, triclosan,.... Trên thực tế, chúng tôi cấm hàng trăm thành phần nguy hại và thường xuyên cập nhật danh sách này theo tiêu chuẩn của bộ y tế Việt Nam. Chúng tôi luôn luôn làm điều này vì chúng tôi tôn trọng luật pháp và làn da của bạn.

100% thuần chay: chúng tôi không sử dụng các nguyên liệu có nguồn gốc từ động vật thường thấy trong mỹ phẩm như : mật ong, sáp ong, mỡ lông cừu, nhau thai cừu, dịch ốc sên, dầu gan cá mập, tơ tằm,.. Thay vào đó chúng tôi vận dụng và phát huy tối đa kha năng của các hoạt chất, chiết xuất từ thực vật mà không cần đến sự hỗ trợ của các nguyên liệu có nguồn gốc từ động vật.

100% không bao giờ thử nghiệm trên động vật: các công thức mỹ phẩm của Cocoon được nghiên cứu và được thử nghiệm bằng các bài kiểm tra trong phòng thí nghiệm (in-Vitro test) hoặc trên các tình nguyện viên (in-Vivo test). Đồng thời các nhà cung cấp nguyên liệu cũng song hành và cam kết rằng họ cũng không thử nghiệm trên động vật trong quá trình nghiên cứu và sản xuất ra nguyên liệu đó. Chúng tôi không thực hiện các bài thử nghiệm lên động vật như: thỏ, chuột, lòng đỏ trứng gà đã thụ tinh,... vì tính nhân đạo và lời hứa bất di bất dịch với khách hàng và cộng đồng.

</p>
